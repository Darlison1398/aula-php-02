<?php
 
// Switch, pode substituir o if. Mas não é aplicável em todos os casos. Dev-se tomar cuidado!!!
//  Todo o case do Switch deve ter um break par aparar a execução.
// podemos adicionar o default que seria quase que um else. ele é o último case

$x = 0;

switch ($x){
    case 0:
        echo "x é igual a 0 <br>";
        break;
    case 1:
        echo "x é igual a 1 <br>";
        break;
}

